function switchIt(num: number) {
  switch (num) {
    case 1:
      console.log("One");
      break;

    case 2:
      console.log("Two");
      break;
    case 3:
      console.log("Three");
      break;
    case 4:
      console.log("Four");
      break;
    case 5:
      console.log("Five");
      break;
    default:
      console.log("Invalid Number");
  }
}

switchIt(3);
switchIt(5);
switchIt(2);
switchIt(0);
